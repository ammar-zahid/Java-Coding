package Lecture_2;

public class BasicMaths {
    public static void main(String[] args) {
        int x = 5;
        int y = 7;

        int addition = x + y;
        int subtraction = y - x;
        int multiplication = x * y;
        int division = y / x;

        System.out.println(addition);
        System.out.println(subtraction);
        System.out.println(multiplication);
        System.out.println(division);
    }
}
